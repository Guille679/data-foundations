# Ejercicio 5 - Resto de la división por 2

# EJERCICIO DADO -- Crear un programa que le solicite al usuario un número, y que imprima el resto obtenido de dividirlo por 2.

# Solicitud de número
numero = int(input("Ingresa un número entero: "))

# Cálculo del resto dividido por 2
resultado_resto = numero % 2

# Impresión del resultado
print(f"El resto de dividir {numero} por 2 es: {resultado_resto}")
